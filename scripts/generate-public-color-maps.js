function windScale(min, max, fadeEnd, colors) {
  const steps = [];
  const step = Math.max(1, Math.round((max - min) / 12));
  for (let value = min; value <= max; value += step) steps.push(value);
  if (!steps.includes(max)) steps.push(max);
  return steps.map((value) => [
    value,
    interpolatePalette(colors, (value - min) / Math.max(1e-9, max - min)),
    value <= fadeEnd ? 0.132 : 1,
  ]);
}

function interpolateValueStops(anchors, step = 1) {
  const min = Math.min(...anchors.map(([value]) => value));
  const max = Math.max(...anchors.map(([value]) => value));
  const out = [];
  for (let value = min; value <= max + 1e-9; value += step)
    out.push([Math.abs(value) < 1e-9 ? 0 : Number(value.toFixed(6)), rgbToHex(interpolateAnchors(anchors, value))]);
  return out;
}

function withBreaks(rows, breaks) {
  const out = [...rows];
  for (const [value, lowerColor, upperColor, alpha = null] of breaks) {
    out.push([value, colorToOutput(lowerColor), alphaFromColor(lowerColor, alpha)]);
    out.push([value, colorToOutput(upperColor), alphaFromColor(upperColor, alpha)]);
  }
  return out.map(normalizeOutputStop).sort((left, right) => left[0] - right[0]);
}

function normalizeOutputStop(row) {
  const value = Number(row[0]);
  const color = colorToOutput(row[1]);
  const alpha = Number.isFinite(Number(row[2])) ? Number(row[2]) : alphaFromColor(row[1], null);
  return Number.isFinite(alpha) ? [value, color, roundAlpha(alpha)] : [value, color];
}

function colorToOutput(color) {
  return Array.isArray(color) ? color.slice(0, 3).map((value) => clampInt(value, 0, 255)) : normalizeHex(color);
}

function alphaFromColor(color, fallback) {
  return Array.isArray(color) && Number.isFinite(Number(color[3])) ? Number(color[3]) : fallback;
}

function interpolateAnchors(anchors, value) {
  const sorted = anchors
    .map(([anchorValue, color]) => [Number(anchorValue), rgba(color)])
    .filter(([anchorValue]) => Number.isFinite(anchorValue))
    .sort((left, right) => left[0] - right[0]);
  if (value <= sorted[0][0]) return [...sorted[0][1]];
  for (let index = 1; index < sorted.length; index += 1) {
    const [rightValue, rightColor] = sorted[index];
    const [leftValue, leftColor] = sorted[index - 1];
    if (value <= rightValue)
      return mixRgba(leftColor, rightColor, (value - leftValue) / Math.max(1e-9, rightValue - leftValue));
  }
  return [...sorted[sorted.length - 1][1]];
}

function interpolatePalette(colors, position) {
  return rgbToHex(
    interpolateAnchors(
      colors.map((color, index) => [index / Math.max(1, colors.length - 1), color]),
      Math.max(0, Math.min(1, position)),
    ),
  );
}

function mixRgba(left, right, t) {
  const leftAlpha = Number.isFinite(Number(left[3])) ? left[3] : 1;
  const rightAlpha = Number.isFinite(Number(right[3])) ? right[3] : 1;
  const alpha = leftAlpha + (rightAlpha - leftAlpha) * t;
  if (alpha <= 1e-9)
    return [
      Math.round(left[0] + (right[0] - left[0]) * t),
      Math.round(left[1] + (right[1] - left[1]) * t),
      Math.round(left[2] + (right[2] - left[2]) * t),
      0,
    ];
  const channels = [0, 1, 2].map((index) =>
    Math.round((left[index] * leftAlpha + (right[index] * rightAlpha - left[index] * leftAlpha) * t) / alpha),
  );
  channels.push(roundAlpha(alpha));
  return channels;
}

function normalizeStops(valueStops, min, max) {
  return valueStops.map(([value, color, alpha]) => {
    const colorValue = rgba(color);
    const resolvedAlpha = Number.isFinite(Number(alpha))
      ? Number(alpha)
      : Number.isFinite(Number(colorValue[3]))
        ? colorValue[3]
        : 1;
    return [
      roundPosition((Number(value) - min) / Math.max(1e-9, max - min)),
      [colorValue[0], colorValue[1], colorValue[2], roundAlpha(resolvedAlpha)],
    ];
  });
}

function colorRecord(rgb, alpha) {
  const color = rgba(rgb);
  const resolved = [color[0], color[1], color[2]];
  const roundedAlpha = roundAlpha(alpha);
  return {
    hex: rgbToHex(resolved),
    rgb: resolved,
    alpha: roundedAlpha,
    cssRgba: `rgba(${resolved[0]}, ${resolved[1]}, ${resolved[2]}, ${roundedAlpha.toFixed(3)})`,
  };
}

function rgba(color) {
  if (Array.isArray(color))
    return [
      clampInt(color[0], 0, 255),
      clampInt(color[1], 0, 255),
      clampInt(color[2], 0, 255),
      Number.isFinite(Number(color[3])) ? roundAlpha(Number(color[3])) : 1,
    ];
  const rgb = hexToRgb(color);
  return [rgb[0], rgb[1], rgb[2], 1];
}

function hexToRgb(input) {
  const hex = normalizeHex(input).slice(1);
  const value = Number.parseInt(hex, 16);
  return [(value >> 16) & 255, (value >> 8) & 255, value & 255];
}

function rgbToHex(color) {
  const rgb = rgba(color);
  return `#${rgb
    .slice(0, 3)
    .map((value) => clampInt(value, 0, 255).toString(16).padStart(2, "0"))
    .join("")}`.toUpperCase();
}

function normalizeHex(input) {
  const text = String(input || "").trim();
  if (!/^#?[0-9a-fA-F]{6}$/.test(text)) throw new Error(`Invalid hex color: ${input}`);
  return `#${text.replace(/^#/, "").toUpperCase()}`;
}

function uniqueSorted(values) {
  return [...new Set(values.map((value) => Number(value)).filter(Number.isFinite))].sort((left, right) => left - right);
}

function stablePayloadHash(payload) {
  const clone = JSON.parse(JSON.stringify(payload));
  delete clone.sourceSha256;
  return crypto.createHash("sha256").update(JSON.stringify(clone)).digest("hex");
}

